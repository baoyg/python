# 第2章 变量和简单数据类型

# 2.4 数字

# 2.4.1 整数
# 可对整数执行加(+ )减(- )乘(* )除(/ )运算。
a = 2 + 3
print(a)

a = 3 - 2
print(a)

a = 2 * 3
print(a)

a = 3 / 2
print(a)

a = 3 ** 2
print(a)

a = 2 + 3 * 4
print(a)

# 2.4.2 浮点数
# Python将带小数点的都成为浮点数

a = 0.1 + 0.1
print(a)

a = 2 * 0.1
print(a)

# 注意，结果包含的小数位数可能是不确定的
a = 0.2 + 0.1
print(a)
# >>> 0.30000000000000004
# 所有语言都存在这种问题，不必担心，暂时忽略多余的小数